// 1 - Crie um array com 5 nomes de países

const paises = ["Brasil", "Argentina", "Africa do Sul", "Paraguai", "Chile"];

// 2- Adicione um país ao fim da lista

paises.push("Peru");

// 3- Imprima a lista na tela

console.log(paises)

// 4- Remova um país do fim da lista

paises.pop();

// 5- Imprima a lista na tela

console.log(paises);

// 6- Adicione um país o início da lista

paises.unshift("Peru");

// 7- Imprima a lista na tela
console.log(paises);

// 8- Remova um país do início da lista
paises.shift("Peru");

// 9- Imprima o último país da lista na tela

console.log(paises[paises.length -1])

// 10 - Imprima o segundo país da lista na tela
