/* Faça um programa que imprima todos os múltiplos do número 3 entre 0 e 50.

Múltiplos de 3: 0, 3, 6, 9, 12... */

let numero = 3;

while (numero <= 50) {
    console.log(numero);
    numero += 3 //ou numero = numero + 3
}

